# 큰 따옴표 " "
# 작은 따옴표 ' '
# 큰 따옴표 문장 """ """
# 작은 따옴표 문장 ''' '''

a = "Hello World!"
b = 'Hello World!'
c = """Hello
World!"""
d = '''Hello
World!'''

print(a)
print(b)
print(c)
print(d)
print()
# 문자열에 따옴표 포함시키기

food = "Python's favorite food is perl"
say = '"Python is very easy." he says.'

print(food)
print(say)
print()
# Escape Sequence

food = 'Python\'s favorite food is perl'
say = "\"Python is very easy.\" he says."

print(food)
print(say)
print()

c = "Hello\nWorld!"
print(c)
print()

# 문자열 더하기

a = "Hello"
b = " World!"

print(a+b)
print()

# 문자열 곱하기
c = a + b
print(c * 10)
print()
